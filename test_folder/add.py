def add(a: int, b: int) -> int:
    """Adds two numbers.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The sum of a and b.
    """
    return a + b

print(add(1, 2))  # Output: 3
print(add(3, 4))  # Output: 7
print(add(5, 6))  # Output: 11
